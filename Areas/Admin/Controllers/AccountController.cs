        [HttpPost]
        [AllowAnonymous]
        public JsonResult UpdateAvatar(string avatar, string id)
        {
            var status = false;
            var model = _identityContext.AspNetUsers.Find(id);
            model.Avatar = avatar;
            try
            {
                _identityContext.SaveChanges();
                InitSession(model);
                status = true;
            }
            catch
            {
                status = false;
            }

            return Json(new
            {
                status = status
            });
        }

        private void InitSession(AspNetUser model)
        {
            AspNetUser user = new AspNetUser();
            user.UserName = model.UserName;
            user.Avatar = model.Avatar;
            user.Email = model.Email;
            user.Id = model.Id;
            Session.Add(CommonConstants.USER_SESSION, user);
        }

