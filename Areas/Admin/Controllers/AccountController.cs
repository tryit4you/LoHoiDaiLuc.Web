        // Product: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }
            AspNetUser user = _identityContext.AspNetUsers.Where(x => x.Email == model.Email).SingleOrDefault();
            if (user == null)
            {
                ModelState.AddModelError("CustomError", "Tài khoản không tồn tại");
                return View(model);
            }
            else
                if (user.EmailConfirmed == false)
            {
                ModelState.AddModelError("CustomError", "Tài khoản chưa được xác thực");
                return View(model);
            }
            // This doesn't count login failures towards account lockout
            // To enable password failures to trigger account lockout, change to shouldLockout: true
            var result = await SignInManager.PasswordSignInAsync(model.Email, model.Password, model.RememberMe, shouldLockout: false);
            switch (result)
            {
                case SignInStatus.Success:
                    {
                        var sessionUser = _identityContext.AspNetUsers.Where(x => x.Email == model.Email).Single();
                        if (sessionUser != null)
                        {
                            InitSession(sessionUser);
                        }
                        return RedirectToLocal(returnUrl);
                    }
                case SignInStatus.LockedOut:
                    return View("Lockout");

                case SignInStatus.RequiresVerification:
                    return RedirectToAction("SendCode", new { ReturnUrl = returnUrl, RememberMe = model.RememberMe });

                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Tên đăng nhập hoặc mật khẩu không đúng!");
                    return View(model);
            }
        }

